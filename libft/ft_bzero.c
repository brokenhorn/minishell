#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*d;

	d = (char *)s;
	if (n == 0)
		return ;
	while (n != 0)
	{
		*d = '\0';
		d++;
		n--;
	}
	return ;
}
